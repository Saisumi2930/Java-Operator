
public class Logical {
	public static void main(String[] args) {
		int a=2;
		int b=3;
		
		if(a<3 && b>4)
			System.out.println("True");
		else
			System.out.println("False");
		
		if(a<3 || b>4)
			System.out.println("True");
		else
			System.out.println("False");
		
		if(a<b != b>a)
			System.out.println("True");
		else
			System.out.println("False");
		
		
		
		
	}

}
